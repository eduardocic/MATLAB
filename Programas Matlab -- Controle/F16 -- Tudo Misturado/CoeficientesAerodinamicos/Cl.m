 S = 0.2*alpha;
 K = fix(S);
%  K = floor(S);
 
 if(K <= -2)
     K = -1;
 end
 if(K >= 9)
     K = 8;
 end
 
 DA = S - K;
 L = K + fix(1.1*sign(DA));
 
 S = 0.2*abs(beta);
 M = fix(S);
%  M = floor(S);
 if( M == 0)
     M = 1;
 end
 if( M >= 6)
     M = 5;
 end
 DB = S - M;
 N = M + fix(1.1*sign(DB));
 
 % -------------------------------------------------------------------------
 % Compensacao por software
 K = K + 3;
 L = L + 3;
 M = M + 1;
 N = N + 1;
 % -------------------------------------------------------------------------
 
%  T = A(K, M);
%  U = A(K, N);
%  V = T + abs(DA)*(A(L, M) - T);
%  W = U + abs(DA)*(A(L, N) - U);
 
 T = A(M, K);
 U = A(N, K);
 V = T + abs(DA)*(A(M, L) - T);
 W = U + abs(DA)*(A(N, L) - U);


 DUM = V + (W - V)*abs(DB);
 
 CoeficienteRollingMoment = DUM*sign(beta);
 
 
end
 
    
    