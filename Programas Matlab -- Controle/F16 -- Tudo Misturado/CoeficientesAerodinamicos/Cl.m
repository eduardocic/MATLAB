%        -0.001  -0.004  -0.008  -0.012  -0.016  -0.019  -0.020  -0.020  -0.015  -0.080  -0.013  -0.015;
%        -0.003  -0.009  -0.017  -0.024  -0.030  -0.034  -0.040  -0.037  -0.016  -0.002  -0.010  -0.019;
