       -.001,  -.010,  -.020,  -.030,  -.039,  -.044,  -.050,  -.049,  -.023,  -.006,  -.014,  -.027;
        .000,  -.010,  -.022,  -.034,  -.047,  -.046,  -.059,  -.061,  -.033,  -.036,  -.035,  -.035;
        .007,  -.010,  -.023,  -.034,  -.049,  -.046,  -.068,  -.071,  -.060,  -.058,  -.062,  -.059;
        .009,  -.011,  -.023,  -.037,  -.050,  -.047,  -.074,  -.079,  -.091,  -.076,  -.077,  -.076];


% 
